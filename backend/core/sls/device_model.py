    def __init__(self, deviceName: str, portName: str, baud: int, ADDR: int = 0x50):
        """
        初始化设备模型
        
        Args:
            deviceName: 设备名称
            portName: 串口号
            baud: 波特率
            ADDR: Modbus设备地址
        """
        self.deviceName = deviceName
        self.serialConfig = SerialConfig()
        self.serialConfig.portName = portName
        self.serialConfig.baud = baud
        self.ADDR = ADDR
        
        # 设备状态
        self.isOpen = False
        self.serialPort: Optional[serial.Serial] = None
        
        # 数据存储
        self.deviceData: Dict[str, Any] = {}
        self.TempBytes = []
        self.statReg = None
        
        # 循环读取控制
        self.loop = False
        self.readThread: Optional[threading.Thread] = None
    
    def get_crc(self, datas, dlen):
        """计算CRC校验"""
        tempH = 0xff
        tempL = 0xff
        for i in range(0, dlen):
            tempIndex = (tempH ^ datas[i]) & 0xff
            tempH = (tempL ^ self.auchCRCHi[tempIndex]) & 0xff
            tempL = self.auchCRCLo[tempIndex]
        return (tempH << 8) | tempL
    
    def set(self, key: str, value: Any):
        """设置设备数据"""
        self.deviceData[key] = value
    
    def get(self, key: str) -> Optional[Any]:
        """获取设备数据"""
        return self.deviceData.get(key)
    
    def openDevice(self) -> bool:
        """打开设备"""
        self.closeDevice()
        try:
            self.serialPort = serial.Serial(
                self.serialConfig.portName,
                self.serialConfig.baud,
                timeout=0.5
            )
            if self.serialPort.is_open:
                self.isOpen = True
                print(f"✅ {self.deviceName} 已连接到 {self.serialConfig.portName}")
                return True
            else:
                return False
        except Exception as e:
            print(f"❌ 打开设备失败: {e}")
            return False
    
    def closeDevice(self):
        """关闭设备"""
        self.stopLoopRead()
        if self.serialPort:
            try:
                self.serialPort.close()
            except:
                pass
            self.serialPort = None
        self.isOpen = False
    
    def sendData(self, data: bytes):
        """发送数据"""
        if self.serialPort and self.serialPort.is_open:
            self.serialPort.write(data)
    
    def readDataTh(self, threadName: str, interval: float):
        """数据读取线程"""
        while self.loop:
            try:
                if self.serialPort and self.serialPort.is_open:
                    data = self.serialPort.read(1024)
                    if data:
                        self.TempBytes.extend(data)
                        self.process_data(len(data))
                time.sleep(interval)
            except Exception as e:
                print(f"读取数据错误: {e}")
                time.sleep(1)
    
