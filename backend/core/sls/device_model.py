    def startLoopRead(self):
        """开始循环读取"""
        if not self.loop:
            self.loop = True
            self.readThread = threading.Thread(
                target=self.readDataTh,
                args=("Data-Received-Thread", 0.01),
                daemon=True
            )
            self.readThread.start()
    
    def stopLoopRead(self):
        """停止循环读取"""
        self.loop = False
        if self.readThread and self.readThread.is_alive():
            self.readThread.join(timeout=1.0)


